export async function listUpcomingLocalDividends(
  userId: string,
  fromDate: Date
): Promise<Dividend[]> {
  const rows = await prisma.dividend.findMany({
    where: {
      userId,
      paymentDate: {
        gte: fromDate,
      },
    },
    orderBy: [{ paymentDate: "asc" }, { createdAt: "asc" }],
  });

  return rows.map(mapDividendRow);
}

export async function hasLocalDuplicateDividend(
  userId: string,
  assetId: string,
  startDate: Date,
  endDate: Date
): Promise<boolean> {
  const row = await prisma.dividend.findFirst({
    where: {
      userId,
      assetId,
      exDate: {
        gte: startDate,
        lte: endDate,
      },
    },
  });

  return Boolean(row);
}

export async function deleteUpcomingLocalAutoDividendsForAsset(
  userId: string,
  assetId: string,
  dividendType: Dividend["dividendType"],
  fromDate?: Date
): Promise<number> {
  const result = await prisma.dividend.deleteMany({
    where: {
      userId,
      assetId,
      dividendType,
      isAutoGenerated: true,
      ...(fromDate
        ? {
            paymentDate: {
              gte: fromDate,
            },
          }
        : {}),
    },
  });

  return result.count;
}

function buildDividendWhere(
  userId: string,
  filters: LocalDividendListFilters
): Prisma.DividendWhereInput {
  const where: Prisma.DividendWhereInput = { userId };

  if (filters.assetId) {
    where.assetId = filters.assetId;
  }

  if (filters.startDate || filters.endDate) {
    where.paymentDate = {
      gte: filters.startDate,
      lte: filters.endDate,
    };
  }

  return where;
}

function buildDividendData(
  dividendData: DividendFormData,
  asset?: AssetRow | null,
  overrides: {
    assetTicker?: string;
    assetName?: string;
    assetIsin?: string;
    isAutoGenerated?: boolean;
    grossAmountEur?: number;
    taxAmountEur?: number;
    netAmountEur?: number;
    exchangeRate?: number;
    legacyFirebaseId?: string;
  } = {}
): Omit<Prisma.DividendUncheckedCreateInput, "userId"> {
  const metadata = getAssetMetadata(asset);
  const assetTicker = overrides.assetTicker ?? asset?.ticker;
  const assetName = overrides.assetName ?? asset?.name;

  return {
    assetId: dividendData.assetId,
    assetTicker: assetTicker ?? "",
    assetName: assetName ?? "",
    assetIsin: overrides.assetIsin ?? getAssetIsin(metadata),
    exDate: dividendData.exDate,
    paymentDate: dividendData.paymentDate,
    dividendPerShare: dividendData.dividendPerShare,
    quantity: dividendData.quantity,
    grossAmount: dividendData.grossAmount,
    taxAmount: dividendData.taxAmount,
    netAmount: dividendData.netAmount || (dividendData.grossAmount - dividendData.taxAmount),
    currency: dividendData.currency,
    dividendType: dividendData.dividendType,
    notes: dividendData.notes,
    isAutoGenerated: overrides.isAutoGenerated ?? dividendData.isAutoGenerated,
    grossAmountEur: overrides.grossAmountEur,
    taxAmountEur: overrides.taxAmountEur,
    netAmountEur: overrides.netAmountEur,
    exchangeRate: overrides.exchangeRate,
    costPerShare: dividendData.costPerShare ?? getAverageCost(metadata),
    legacyFirebaseId: overrides.legacyFirebaseId,
  };
}

function buildDividendUpdateData(
  updates: LocalDividendUpdateInput,
  asset?: AssetRow | null
): Prisma.DividendUncheckedUpdateInput {
  const netAmount = updates.netAmount ?? (
    updates.grossAmount !== undefined && updates.taxAmount !== undefined
      ? updates.grossAmount - updates.taxAmount
      : undefined
  );
  const metadata = getAssetMetadata(asset);

  return stripUndefined({
    assetId: updates.assetId,
    assetTicker: asset?.ticker,
    assetName: asset?.name,
    assetIsin: getAssetIsin(metadata),
    exDate: updates.exDate,
    paymentDate: updates.paymentDate,
    dividendPerShare: updates.dividendPerShare,
    quantity: updates.quantity,
    grossAmount: updates.grossAmount,
    taxAmount: updates.taxAmount,
    netAmount,
    currency: updates.currency,
    dividendType: updates.dividendType,
    notes: updates.notes,
    isAutoGenerated: updates.isAutoGenerated,
    expenseId: updates.expenseId,
    grossAmountEur: updates.grossAmountEur,
    taxAmountEur: updates.taxAmountEur,
    netAmountEur: updates.netAmountEur,
    exchangeRate: updates.exchangeRate,
    costPerShare: updates.costPerShare ?? getAverageCost(metadata),
    updatedAt: updates.updatedAt,
  });
}

function mapDividendRow(row: DividendRow): Dividend {
  return {
    id: row.id,
    userId: row.userId,
    assetId: row.assetId,
    assetTicker: row.assetTicker,
    assetName: row.assetName,
    assetIsin: row.assetIsin ?? undefined,
    exDate: row.exDate,
    paymentDate: row.paymentDate,
    dividendPerShare: row.dividendPerShare,
    quantity: row.quantity,
    grossAmount: row.grossAmount,
    taxAmount: row.taxAmount,
    netAmount: row.netAmount,
    currency: row.currency,
    dividendType: row.dividendType as Dividend["dividendType"],
    notes: row.notes ?? undefined,
    isAutoGenerated: row.isAutoGenerated,
    expenseId: row.expenseId ?? undefined,
    grossAmountEur: row.grossAmountEur ?? undefined,
    taxAmountEur: row.taxAmountEur ?? undefined,
    netAmountEur: row.netAmountEur ?? undefined,
    exchangeRate: row.exchangeRate ?? undefined,
    costPerShare: row.costPerShare ?? undefined,
    createdAt: row.createdAt,
    updatedAt: row.updatedAt,
  };
}

function getAverageCost(metadata: Record<string, unknown>): number | undefined {
  return typeof metadata.averageCost === "number" ? metadata.averageCost : undefined;
}

function getAssetIsin(metadata: Record<string, unknown>): string | undefined {
  return typeof metadata.isin === "string" ? metadata.isin : undefined;
}

function getAssetMetadata(asset?: AssetRow | null): Record<string, unknown> {
  return asset && isRecord(asset.metadata) ? asset.metadata : {};
}

function isRecord(input: unknown): input is Record<string, unknown> {
  return typeof input === "object" && input !== null && !Array.isArray(input);
}

function stripUndefined<T extends Record<string, unknown>>(input: T): Partial<T> {
  return Object.fromEntries(
    Object.entries(input).filter(([, value]) => value !== undefined)
  ) as Partial<T>;
}
