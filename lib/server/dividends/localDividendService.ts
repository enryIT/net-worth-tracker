import "server-only";

import { Prisma } from "@prisma/client";
import { prisma } from "@/lib/server/prisma";
import type { Dividend, DividendFormData } from "@/types/dividend";

export type LocalDividendListFilters = {
  assetId?: string;
  startDate?: Date;
  endDate?: Date;
};

export type LocalDividendCreateLegacyInput = {
  dividendData: DividendFormData;
  assetTicker?: string;
  assetName?: string;
  assetIsin?: string;
  isAutoGenerated?: boolean;
  grossAmountEur?: number;
  taxAmountEur?: number;
  netAmountEur?: number;
  exchangeRate?: number;
  legacyDeterministicId?: string;
};

export type LocalDividendUpdateInput = Partial<DividendFormData> & {
  expenseId?: string;
  grossAmountEur?: number;
  taxAmountEur?: number;
  netAmountEur?: number;
  exchangeRate?: number;
  updatedAt?: Date;
};

type AssetRow = {
  id: string;
  ticker: string;
  name: string;
  metadata: Prisma.JsonValue;
};

type DividendRow = {
  id: string;
  userId: string;
  assetId: string;
  assetTicker: string;
  assetName: string;
  assetIsin: string | null;
  exDate: Date;
  paymentDate: Date;
  dividendPerShare: number;
  quantity: number;
  grossAmount: number;
  taxAmount: number;
  netAmount: number;
  currency: string;
  dividendType: string;
  notes: string | null;
  isAutoGenerated: boolean;
  expenseId: string | null;
  grossAmountEur: number | null;
  taxAmountEur: number | null;
  netAmountEur: number | null;
  exchangeRate: number | null;
  costPerShare: number | null;
  createdAt: Date;
  updatedAt: Date;
};

export async function listLocalDividends(
  userId: string,
  filters: LocalDividendListFilters = {}
): Promise<Dividend[]> {
  const rows = await prisma.dividend.findMany({
    where: buildDividendWhere(userId, filters),
    orderBy: [{ paymentDate: "desc" }, { createdAt: "desc" }],
  });

  return rows.map(mapDividendRow);
}

export async function getLocalDividendById(
  userId: string,
  dividendId: string
): Promise<Dividend | null> {
  const row = await prisma.dividend.findFirst({
    where: { id: dividendId, userId },
  });

  return row ? mapDividendRow(row) : null;
}

export async function getLocalDividendByIdAnyUser(
  dividendId: string
): Promise<Dividend | null> {
  const row = await prisma.dividend.findUnique({
    where: { id: dividendId },
  });

  return row ? mapDividendRow(row) : null;
}

export async function createLocalDividend(
  userId: string,
  dividendData: DividendFormData
): Promise<Dividend> {
  const asset = await prisma.asset.findUnique({
    where: { id_userId: { id: dividendData.assetId, userId } },
  });

  if (!asset) {
    throw new Error("Asset not found");
  }

  const row = await prisma.dividend.create({
    data: {
      userId,
      ...buildDividendData(dividendData, asset),
    },
  });

  return mapDividendRow(row);
}

export async function createLocalDividendFromLegacyInput(
  userId: string,
  input: LocalDividendCreateLegacyInput
): Promise<Dividend> {
  const asset = await prisma.asset.findUnique({
    where: { id_userId: { id: input.dividendData.assetId, userId } },
  });

  const assetMetadata = getAssetMetadata(asset);
  const assetTicker = input.assetTicker ?? asset?.ticker;
  const assetName = input.assetName ?? asset?.name;
  const assetIsin = input.assetIsin ?? getAssetIsin(assetMetadata);

  if (!assetTicker || !assetName) {
    throw new Error("Asset not found");
  }

  const data = buildDividendData(input.dividendData, asset, {
    assetTicker,
    assetName,
    assetIsin,
    isAutoGenerated: input.isAutoGenerated,
    grossAmountEur: input.grossAmountEur,
    taxAmountEur: input.taxAmountEur,
    netAmountEur: input.netAmountEur,
    exchangeRate: input.exchangeRate,
    legacyFirebaseId: input.legacyDeterministicId,
  });

  if (input.isAutoGenerated && input.legacyDeterministicId) {
    const row = await prisma.dividend.upsert({
      where: {
        userId_legacyFirebaseId: {
          userId,
          legacyFirebaseId: input.legacyDeterministicId,
        },
      },
      update: {
        ...data,
        updatedAt: new Date(),
      },
      create: {
        userId,
        ...data,
      },
    });

    return mapDividendRow(row);
  }

  const row = await prisma.dividend.create({
    data: {
      userId,
      ...data,
    },
  });

  return mapDividendRow(row);
}

export async function updateLocalDividend(
  userId: string,
  dividendId: string,
  updates: LocalDividendUpdateInput
): Promise<Dividend | null> {
  try {
    const asset = updates.assetId
      ? await prisma.asset.findUnique({
          where: { id_userId: { id: updates.assetId, userId } },
        })
      : undefined;

    if (updates.assetId && !asset) {
      throw new Error("Asset not found");
    }

    const row = await prisma.dividend.update({
      where: { id_userId: { id: dividendId, userId } },
      data: buildDividendUpdateData(updates, asset),
    });

    return mapDividendRow(row);
  } catch (error) {
    if (error instanceof Prisma.PrismaClientKnownRequestError && error.code === "P2025") {
      return null;
    }

    throw error;
  }
}

